// === SPARKLE EXPLOSION ON ENVELOPE OPEN ===
function createSparkleExplosion() {
    const envelope = document.querySelector('.envelope-container');
    const rect = envelope.getBoundingClientRect();
    const centerX = rect.left + rect.width / 2;
    const centerY = rect.top + rect.height / 2;
    
    for (let i = 0; i < 30; i++) {
        const sparkle = document.createElement('div');
        sparkle.style.cssText = `
            position: fixed;
            left: ${centerX}px;
            top: ${centerY}px;
            width: 8px;
            height: 8px;
            border-radius: 50%;
            background: ${['#ff6b8a', '#ffd700', '#ff8fab', '#ffa0b4', '#fff'][Math.floor(Math.random() * 5)]};
            pointer-events: none;
            z-index: 1000;
            box-shadow: 0 0 6px currentColor;
        `;
        document.body.appendChild(sparkle);
        
        const angle = (Math.PI * 2 * i) / 30;
        const distance = Math.random() * 150 + 50;
        const dx = Math.cos(angle) * distance;
        const dy = Math.sin(angle) * distance;
        
        sparkle.animate([
            { transform: 'translate(0, 0) scale(1)', opacity: 1 },
            { transform: `translate(${dx}px, ${dy}px) scale(0)`, opacity: 0 }
        ], {
            duration: 800 + Math.random() * 400,
            easing: 'cubic-bezier(0, 0.5, 0.5, 1)',
            fill: 'forwards'
        });
        
        setTimeout(() => sparkle.remove(), 1500);
    }
}

// === DISPLAY LETTER WITH REASON BUTTON AFTER LINE 2 ===
function displayLetterContent() {
    const container = document.getElementById('letter-content');
    // Split by lines, insert reason button after line 2
    const lines = letterText.split('\n');
    
    // Build HTML: lines before button + button + remaining lines
    let beforeBtn = [];
    let afterBtn = [];
    let lineCount = 0;
    
    for (let i = 0; i < lines.length; i++) {
        // Count non-empty lines as "real" paragraphs
        if (lines[i].trim() !== '') {
            lineCount++;
        }
        if (lineCount <= 2) {
            beforeBtn.push(lines[i]);
        } else {
            afterBtn.push(lines[i]);
        }
    }
    
    // Create elements
    const beforeSpan = document.createElement('span');
    beforeSpan.textContent = beforeBtn.join('\n');
    
    const reasonBtn = document.createElement('button');
    reasonBtn.className = 'btn-reason';
    reasonBtn.innerHTML = '💭 Xem lý do';
    reasonBtn.onclick = openReasonModal;
    
    const afterSpan = document.createElement('span');
    afterSpan.textContent = '\n' + afterBtn.join('\n');
    
    container.innerHTML = '';
    container.appendChild(beforeSpan);
    container.appendChild(document.createElement('br'));
    container.appendChild(reasonBtn);
    container.appendChild(afterSpan);
}

