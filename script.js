// === MOVE THE "NO" BUTTON AWAY ===
let noMoveCount = 0;

function moveNoButton(e) {
    // Prevent default touch behavior (prevents triggering click after touch)
    if (e && e.type === 'touchstart') {
        e.preventDefault();
        e.stopPropagation();
    }
    
    const btn = document.getElementById('btn-no');
    
    noMoveCount++;
    
    // Move button to body on first dodge so it's not trapped in a stacking context
    if (btn.parentElement !== document.body) {
        document.body.appendChild(btn);
    }
    
    // Calculate safe bounds (viewport)
    const viewW = window.innerWidth;
    const viewH = window.innerHeight;
    const btnW = btn.offsetWidth;
    const btnH = btn.offsetHeight;
    
    // Collect all elements to avoid overlapping
    const avoidElements = [];
    const yesBtn = document.getElementById('btn-yes');
    const questionText = document.getElementById('question-text');
    const heartBig = document.querySelector('.question-heart-big');
    
    if (yesBtn) avoidElements.push(yesBtn.getBoundingClientRect());
    if (questionText) avoidElements.push(questionText.getBoundingClientRect());
    if (heartBig) avoidElements.push(heartBig.getBoundingClientRect());
    
    const padding = 20;
    const buffer = 20;
    
    // Try up to 50 times to find a non-overlapping position
    let newX, newY;
    let found = false;
    
    for (let attempt = 0; attempt < 50; attempt++) {
        newX = Math.random() * (viewW - btnW - padding * 2) + padding;
        newY = Math.random() * (viewH - btnH - padding * 2) + padding;
        
        let hasOverlap = false;
        for (const rect of avoidElements) {
            if (!(newX + btnW + buffer < rect.left || 
                  newX > rect.right + buffer || 
                  newY + btnH + buffer < rect.top || 
                  newY > rect.bottom + buffer)) {
                hasOverlap = true;
                break;
            }
        }
        
        if (!hasOverlap) {
            found = true;
            break;
        }
    }
    
    // Fallback: place at a corner if no safe spot found
    if (!found) {
        newX = padding;
        newY = viewH - btnH - padding;
    }
    
    btn.style.position = 'fixed';
    btn.style.left = newX + 'px';
    btn.style.top = newY + 'px';
    btn.style.zIndex = '99999';
    btn.style.transition = 'left 0.3s cubic-bezier(0.34, 1.56, 0.64, 1), top 0.3s cubic-bezier(0.34, 1.56, 0.64, 1)';
    
    // Show fun messages BELOW the button (not replacing button text)
    const msg = document.getElementById('no-message');
    if (noMoveCount >= 3 && noMoveCount < 6) {
        msg.textContent = 'Đừng mà 🥺';
    } else if (noMoveCount >= 6 && noMoveCount < 10) {
        msg.textContent = 'Bấm Có đi 😢';
    } else if (noMoveCount >= 10 && noMoveCount < 15) {
        msg.textContent = 'Anh xin em 😭';
    } else if (noMoveCount >= 15) {
        // Shake then launch like a rocket and disappear!
        msg.textContent = '';
        btn.querySelector('span').textContent = 'Bực mình rồi nhé 😤';
        btn.onmouseover = null;
        btn.ontouchstart = null;
        btn.onclick = null;
        btn.style.animation = 'shake 0.15s ease-in-out 8';
        btn.style.background = 'rgba(255, 80, 80, 0.25)';
        btn.style.borderColor = 'rgba(255, 80, 80, 0.5)';
        btn.style.color = '#ff8080';
        
        // After shake, launch like a rocket
        setTimeout(() => {
            btn.style.animation = 'none';
            btn.style.transition = 'all 0.6s cubic-bezier(0.55, -0.5, 0.1, 0.9)';
            btn.style.transform = 'translateY(-120vh) rotate(720deg) scale(0.1)';
            btn.style.opacity = '0';
            
            // Remove after animation
            setTimeout(() => {
                btn.remove();
            }, 700);
        }, 1200);
        
        return;
    }
    
    // Make yes button bigger over time
    const yesScale = 1 + (noMoveCount * 0.05);
    yesBtn.style.transform = `scale(${Math.min(yesScale, 1.5)})`;
}

